package com.longwang.uhrm.Entity;


public class SalaryParameters {

  private long idSalaryParameters;
  private String levelSalaryParameters;
  private String papSalaryParameters;
  private String insuranceSalaryParameters;
  private String taxFundamentalSalaryParameters;


  public long getIdSalaryParameters() {
    return idSalaryParameters;
  }

  public void setIdSalaryParameters(long idSalaryParameters) {
    this.idSalaryParameters = idSalaryParameters;
  }


  public String getLevelSalaryParameters() {
    return levelSalaryParameters;
  }

  public void setLevelSalaryParameters(String levelSalaryParameters) {
    this.levelSalaryParameters = levelSalaryParameters;
  }


  public String getPapSalaryParameters() {
    return papSalaryParameters;
  }

  public void setPapSalaryParameters(String papSalaryParameters) {
    this.papSalaryParameters = papSalaryParameters;
  }


  public String getInsuranceSalaryParameters() {
    return insuranceSalaryParameters;
  }

  public void setInsuranceSalaryParameters(String insuranceSalaryParameters) {
    this.insuranceSalaryParameters = insuranceSalaryParameters;
  }


  public String getTaxFundamentalSalaryParameters() {
    return taxFundamentalSalaryParameters;
  }

  public void setTaxFundamentalSalaryParameters(String taxFundamentalSalaryParameters) {
    this.taxFundamentalSalaryParameters = taxFundamentalSalaryParameters;
  }

}
